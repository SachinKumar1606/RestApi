package parseJSON;

public class State {
    private String teh;
    private String dist;
    private String state;

    public String getTeh() {
        return teh;
    }

    public void setTeh(String teh) {
        this.teh = teh;
    }

    public String getDist() {
        return dist;
    }

    public void setDist(String dist) {
        this.dist = dist;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

}
